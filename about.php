    <h2>BACKGROUND</h2>
    <p class="about2">The Conjuring Arts Research Center is a not-for-profit organization dedicated to the preservation and interpretation of magic and its allied arts,
        which include psychic phenomenon, hypnosis, deceptive gambling, mentalism, ventriloquism, juggling, and sleight-of-hand techniques.
        Currently, Conjuring Arts functions primarily as a research library, of over 11,000 book and magazine titles, dating back to the 15th century
        and representing multiple languages. 
        However, the library itself is only the first in a long list of roles Conjuring Arts plays. By providing its holdings, 
        online database, semi-annually published journal, and performance component to the public, 
        Conjuring Arts strives to preserve the rich histories and traditions of this ancient performing art</p>
